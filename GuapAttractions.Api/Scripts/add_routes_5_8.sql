SELECT @routeId = [id_маршрута] FROM [маршрут] WHERE [название] = N'Променад по линиям Васильевского острова';

-- Точки маршрута 5 (минуты по умолчанию = 10)
DECLARE @r5 TABLE([ord] INT, [title] NVARCHAR(300));
INSERT INTO @r5 ([ord],[title]) VALUES
 (1, N'Центральный военно-морской музей имени императора Петра Великого'),
 (2, N'Особняк Румянцева'),
 (3, N'Музей-квартира И.П. Павлова'),
