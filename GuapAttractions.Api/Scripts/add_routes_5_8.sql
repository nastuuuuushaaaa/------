 (10, N'Екатерининская лютеранская церковь (Кирха Св. Екатерины)');

DECLARE r5_cur CURSOR LOCAL FAST_FORWARD FOR
  SELECT [ord],[title] FROM @r5 ORDER BY [ord];
DECLARE @ord INT, @title NVARCHAR(300);
OPEN r5_cur;
FETCH NEXT FROM r5_cur INTO @ord, @title;
WHILE @@FETCH_STATUS = 0
BEGIN
  IF NOT EXISTS (SELECT 1 FROM [достопримечательность] WHERE [название] = @title)
  BEGIN
    INSERT INTO [достопримечательность]
      ([название],[адрес],[описание],[url_аудиофайла])
    VALUES
      (@title, NULL, NULL, NULL);
  END

  SELECT @attractionId = [id_достопримечательности]
  FROM [достопримечательность]
  WHERE [название] = @title;

  IF NOT EXISTS (
    SELECT 1
    FROM [точка_маршрута]
    WHERE [id_маршрута] = @routeId AND [порядок_следования] = @ord
  )
  BEGIN
    INSERT INTO [точка_маршрута]
      ([id_маршрута],[id_достопримечательности],[порядок_следования],[время_для_точки_мин])
    VALUES
      (@routeId, @attractionId, @ord, 10);
  END

  FETCH NEXT FROM r5_cur INTO @ord, @title;
END
CLOSE r5_cur;
DEALLOCATE r5_cur;

