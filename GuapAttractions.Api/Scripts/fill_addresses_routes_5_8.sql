-- Проверка: сколько точек маршрутов 5-8 ещё без адреса
SELECT rp.[id_маршрута], COUNT(1) AS missing
FROM [точка_маршрута] rp
JOIN [достопримечательность] a ON a.[id_достопримечательности] = rp.[id_достопримечательности]
WHERE rp.[id_маршрута] IN (5,6,7,8) AND a.[адрес] IS NULL
GROUP BY rp.[id_маршрута];

