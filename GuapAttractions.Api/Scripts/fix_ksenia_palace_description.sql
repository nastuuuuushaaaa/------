SET NOCOUNT ON;
GO

UPDATE [достопримечательность]
SET [описание] = REPLACE([описание], N'Фасад выходит оформлен', N'Фасад оформлен')
WHERE [название] = N'Дворец великой княгини Ксении Александровны'
  AND [описание] LIKE N'%Фасад выходит оформлен%';
GO
