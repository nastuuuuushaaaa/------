USE [guap_routes_db];

-- 1. Restore correct address for Ново-Михайловский дворец (used by point 11)
UPDATE [достопримечательность]
SET [адрес] = N'Санкт-Петербург, Дворцовая набережная, 18'
WHERE [id_достопримечательности] = 72;

-- 2. Create new attraction for point 7: Дворец великого князя Михаила Михайловича
--    (Адмиралтейская набережная, 8 — different building from Ново-Михайловский)
DECLARE @newId INT;

INSERT INTO [достопримечательность]
    ([название], [адрес], [описание], [url_аудиофайла])
SELECT
    N'Дворец великого князя Михаила Михайловича',
    N'Санкт-Петербург, Адмиралтейская набережная, 8',
    N'Дворец великого князя Михаила Михайловича — один из выдающихся памятников дворцовой архитектуры XIX века на Адмиралтейской набережной Санкт-Петербурга. Построен в 1857–1861 годах по проекту архитектора Гаральда Боссе для великого князя Михаила Михайловича, внука Николая I. Дворец отличается роскошным эклектическим фасадом в духе итальянского ренессанса.',
    [url_аудиофайла]
FROM [достопримечательность]
WHERE [id_достопримечательности] = 72;

SET @newId = SCOPE_IDENTITY();

-- 3. Re-link point 7 of route 7 (pointId=73) to the new attraction
UPDATE [точка_маршрута]
SET [id_достопримечательности] = @newId
WHERE [id_точки_маршрута] = 73;

PRINT N'Готово. Новый id достопримечательности: ' + CAST(@newId AS NVARCHAR);
