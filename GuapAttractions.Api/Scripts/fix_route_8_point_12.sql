Возвышавшаяся над городом доминанта вскоре стала использоваться как пожарная каланча. В 1835 году на башне соорудили специальную конструкцию для подъема сигнальных шаров. На смотровой площадке постоянно дежурили наблюдатели: с помощью флагов, шаров, крестов и фонарей они обозначали часть города, где вспыхнул пожар, и интенсивность горения. Зимой флаги на башне информировали горожан о сильных морозах, а по сигналам гимназисты и школьники могли оставаться дома в непогоду.

В 1839–1854 годах башня стала ключевым звеном самой длинной в мире линии оптического телеграфа, соединявшей Петербург с Варшавой. Протяженность линии составляла 1200 километров и насчитывала 149 станций-башен. С помощью семафорной азбуки сообщение от Петербурга до Варшавы при хорошей погоде доходило всего за 15–20 минут — невероятная скорость для того времени.

