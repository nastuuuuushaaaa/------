DECLARE @basilId INT = (SELECT [id_достопримечательности] FROM [достопримечательность] WHERE [название] = @basilTitle);

IF @basilId IS NULL
BEGIN
  INSERT INTO [достопримечательность]
    ([название],[адрес],[описание],[url_аудиофайла])
  VALUES
    (@basilTitle, NULL,
