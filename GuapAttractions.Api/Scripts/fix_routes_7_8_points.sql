-- Исправление количества точек для маршрутов 7-8 по PDF (Северные и восточные маршруты)
-- Маршрут 7: добавляем 11-ю точку (повторный заголовок "Ново-Михайловский дворец" в конце PDF)
-- Маршрут 8: добавляем 11-ю точку "Базилика Святой Екатерины Александрийской"
--
-- Важно: запускать через sqlcmd с -f 65001 (UTF-8), иначе кириллица ломается.

SET NOCOUNT ON;

DECLARE @route7 INT = (SELECT [id_маршрута] FROM [маршрут] WHERE [название] = N'Парадный Петербург: по набережной к Эрмитажу');
DECLARE @route8 INT = (SELECT [id_маршрута] FROM [маршрут] WHERE [название] = N'Путешествие по набережной Мойки');

IF @route7 IS NULL
BEGIN
  RAISERROR(N'Маршрут 7 не найден.', 16, 1);
  RETURN;
END
IF @route8 IS NULL
BEGIN
  RAISERROR(N'Маршрут 8 не найден.', 16, 1);
  RETURN;
END

--------------------------------------------------------------------------------
-- Route 7: order 11
--------------------------------------------------------------------------------
DECLARE @novoId INT = (SELECT [id_достопримечательности] FROM [достопримечательность] WHERE [название] = N'Ново-Михайловский дворец');
IF @novoId IS NULL
BEGIN
  RAISERROR(N'Не найдена достопримечательность "Ново-Михайловский дворец" для маршрута 7.', 16, 1);
  RETURN;
END

IF NOT EXISTS (SELECT 1 FROM [точка_маршрута] WHERE [id_маршрута] = @route7 AND [порядок_следования] = 11)
BEGIN
  INSERT INTO [точка_маршрута] ([id_маршрута],[id_достопримечательности],[порядок_следования],[время_для_точки_мин])
  VALUES (@route7, @novoId, 11, 10);
END

-- описание (в PDF этот блок повторяется в конце; оставляем тот же текст, что и раньше)
-- если по какой-то причине оно было пустым — заполним кратко
UPDATE a
SET a.[описание] = COALESCE(NULLIF(LTRIM(RTRIM(a.[описание])), N''), a.[описание])
FROM [достопримечательность] a
WHERE a.[id_достопримечательности] = @novoId;

