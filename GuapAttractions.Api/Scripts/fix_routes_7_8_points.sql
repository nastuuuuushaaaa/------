История храма начинается с указа императрицы Екатерины II. В 1763 году она подписала указ о строительстве католического собора для многочисленной иностранной общины Петербурга. Средства на строительство собирали по подписке среди католиков России, значительные суммы пожертвовала сама императрица, а также польский магнат граф Кароль Станислав Радзивилл.

Строительство велось в 1763–1783 годах по проекту архитектора Жана-Батиста Валлен-Деламота, одного из основоположников русского классицизма, автора здания Академии художеств и Малого Эрмитажа.

