USE [guap_routes_db];
GO

UPDATE [маршрут]
SET [название] = N'Прогулка до стрелки Васильевского острова'
WHERE [название] = N'По следам императоров и академиков: путь к стрелке Васильевского острова'
   OR [id_маршрута] = 6;
GO

UPDATE v
SET
  [название] = N'Викторина: Прогулка до стрелки Васильевского острова',
  [описание] = N'Вопросы по маршруту «Прогулка до стрелки Васильевского острова».'
FROM [викторина] v
INNER JOIN [маршрут] m ON m.[id_маршрута] = v.[id_маршрута]
WHERE m.[название] = N'Прогулка до стрелки Васильевского острова';
GO
